DROP TABLE HEE_BUS_SEARCH;

CREATE TABLE HEE_BUS_SEARCH (
    SEARCH_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    BUS_TYPE VARCHAR2(20) NOT NULL,
    BUS_COMPANY VARCHAR2(50),
    BUS_GRADE VARCHAR2(20),
    DEPARTURE_TERMINAL VARCHAR2(100) NOT NULL,
    ARRIVAL_TERMINAL VARCHAR2(100) NOT NULL,
    DEPARTURE_TIME VARCHAR2(10) NOT NULL,
    ARRIVAL_TIME VARCHAR2(10) NOT NULL,
    DURATION VARCHAR2(20),
    PRICE VARCHAR2(20),
    REMAINING_SEATS VARCHAR2(50),
    SEARCH_DATE VARCHAR2(8) NOT NULL,
    CREATED_DATE DATE DEFAULT SYSDATE NOT NULL
);