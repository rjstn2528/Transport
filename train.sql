CREATE TABLE HEE_TRAIN_SEARCH (
    SEARCH_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    TRAIN_TYPE VARCHAR2(20) NOT NULL,
    TRAIN_NUMBER VARCHAR2(10) NOT NULL,
    DEPARTURE_STATION VARCHAR2(50) NOT NULL,
    ARRIVAL_STATION VARCHAR2(50) NOT NULL,
    DEPARTURE_TIME VARCHAR2(10) NOT NULL,
    ARRIVAL_TIME VARCHAR2(10) NOT NULL,
    DURATION VARCHAR2(20),
    GENERAL_PRICE VARCHAR2(20),
    SPECIAL_PRICE VARCHAR2(20),
    AVAILABILITY VARCHAR2(50),
    SEARCH_DATE VARCHAR2(8) NOT NULL,
    CREATED_DATE DATE DEFAULT SYSDATE NOT NULL
);